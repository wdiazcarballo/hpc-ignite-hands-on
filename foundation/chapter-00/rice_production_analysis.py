import numpy as np


def analyze_rice_production(satellite_data: np.ndarray, weather_data: dict) -> float:
    """
    ‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå‡∏ú‡∏•‡∏ú‡∏•‡∏¥‡∏ï‡∏Ç‡πâ‡∏≤‡∏ß‡∏à‡∏≤‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏î‡∏≤‡∏ß‡πÄ‡∏ó‡∏µ‡∏¢‡∏°‡πÅ‡∏•‡∏∞‡∏™‡∏†‡∏≤‡∏û‡∏≠‡∏≤‡∏Å‡∏≤‡∏®

    Parameters
    ----------
    satellite_data : ndarray
        ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏†‡∏≤‡∏û‡∏ñ‡πà‡∏≤‡∏¢‡∏î‡∏≤‡∏ß‡πÄ‡∏ó‡∏µ‡∏¢‡∏° (NDVI - Normalized Difference Vegetation Index)
        ‡∏Ñ‡πà‡∏≤‡∏≠‡∏¢‡∏π‡πà‡∏£‡∏∞‡∏´‡∏ß‡πà‡∏≤‡∏á 0-1 ‡πÇ‡∏î‡∏¢ 1 ‡∏´‡∏°‡∏≤‡∏¢‡∏ñ‡∏∂‡∏á‡∏û‡∏∑‡∏ä‡∏û‡∏£‡∏£‡∏ì‡∏™‡∏°‡∏ö‡∏π‡∏£‡∏ì‡πå
    weather_data : dict
        ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏†‡∏≤‡∏û‡∏≠‡∏≤‡∏Å‡∏≤‡∏® ‡∏õ‡∏£‡∏∞‡∏Å‡∏≠‡∏ö‡∏î‡πâ‡∏ß‡∏¢:
        - temperature: ‡∏≠‡∏∏‡∏ì‡∏´‡∏†‡∏π‡∏°‡∏¥‡πÄ‡∏â‡∏•‡∏µ‡πà‡∏¢ (‡∏≠‡∏á‡∏®‡∏≤‡πÄ‡∏ã‡∏•‡πÄ‡∏ã‡∏µ‡∏¢‡∏™)
        - rainfall: ‡∏õ‡∏£‡∏¥‡∏°‡∏≤‡∏ì‡∏ô‡πâ‡∏≥‡∏ù‡∏ô‡∏£‡∏ß‡∏° (‡∏°‡∏°./‡∏õ‡∏µ)

    Returns
    -------
    float
        ‡∏ú‡∏•‡∏ú‡∏•‡∏¥‡∏ï‡∏Ñ‡∏≤‡∏î‡∏Å‡∏≤‡∏£‡∏ì‡πå (‡∏ï‡∏±‡∏ô/‡πÑ‡∏£‡πà)

    Examples
    --------
    >>> ndvi = np.array([[0.6, 0.7], [0.65, 0.72]])
    >>> weather = {"temperature": 28, "rainfall": 1200}
    >>> yield_est = analyze_rice_production(ndvi, weather)
    >>> print(f"‡∏ú‡∏•‡∏ú‡∏•‡∏¥‡∏ï‡∏Ñ‡∏≤‡∏î‡∏Å‡∏≤‡∏£‡∏ì‡πå: {yield_est:.3f} ‡∏ï‡∏±‡∏ô/‡πÑ‡∏£‡πà")
    """
    # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏î‡∏±‡∏ä‡∏ô‡∏µ‡∏û‡∏∑‡∏ä‡∏û‡∏£‡∏£‡∏ì‡πÄ‡∏â‡∏•‡∏µ‡πà‡∏¢
    ndvi_mean = np.mean(satellite_data)

    # ‡∏õ‡∏±‡∏à‡∏à‡∏±‡∏¢‡∏≠‡∏∏‡∏ì‡∏´‡∏†‡∏π‡∏°‡∏¥ (‡∏Ç‡πâ‡∏≤‡∏ß‡πÄ‡∏ï‡∏¥‡∏ö‡πÇ‡∏ï‡∏î‡∏µ‡∏ó‡∏µ‡πà 25-32¬∞C)
    temp = weather_data.get("temperature", 28)
    if 25 <= temp <= 32:
        temp_factor = 1.0
    elif 20 <= temp < 25 or 32 < temp <= 35:
        temp_factor = 0.9
    else:
        temp_factor = 0.7

    # ‡∏õ‡∏±‡∏à‡∏à‡∏±‡∏¢‡∏õ‡∏£‡∏¥‡∏°‡∏≤‡∏ì‡∏ô‡πâ‡∏≥‡∏ù‡∏ô (‡∏Ç‡πâ‡∏≤‡∏ß‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡∏ô‡πâ‡∏≥ ~1,500 ‡∏°‡∏°./‡∏õ‡∏µ)
    rainfall = weather_data.get("rainfall", 1200)
    optimal_rainfall = 1500
    rain_factor = min(rainfall / optimal_rainfall, 1.2)  # cap at 1.2

    # ‡∏ú‡∏•‡∏ú‡∏•‡∏¥‡∏ï‡∏û‡∏∑‡πâ‡∏ô‡∏ê‡∏≤‡∏ô (‡∏Ñ‡πà‡∏≤‡πÄ‡∏â‡∏•‡∏µ‡πà‡∏¢‡∏õ‡∏£‡∏∞‡πÄ‡∏ó‡∏®‡πÑ‡∏ó‡∏¢ ~0.45 ‡∏ï‡∏±‡∏ô/‡πÑ‡∏£‡πà)
    base_yield = 0.45

    # ‡∏õ‡∏£‡∏∞‡∏°‡∏≤‡∏ì‡∏Å‡∏≤‡∏£‡∏ú‡∏•‡∏ú‡∏•‡∏¥‡∏ï
    estimated_yield = base_yield * ndvi_mean * temp_factor * rain_factor

    return estimated_yield


def generate_sample_data():
    """‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ï‡∏±‡∏ß‡∏≠‡∏¢‡πà‡∏≤‡∏á‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏ó‡∏î‡∏™‡∏≠‡∏ö"""
    # ‡∏à‡∏≥‡∏•‡∏≠‡∏á NDVI data (10x10 grid)
    np.random.seed(42)
    satellite_data = np.random.uniform(0.5, 0.9, size=(10, 10))

    # ‡∏™‡∏†‡∏≤‡∏û‡∏≠‡∏≤‡∏Å‡∏≤‡∏®‡∏ï‡∏±‡∏ß‡∏≠‡∏¢‡πà‡∏≤‡∏á
    weather_data = {
        "temperature": 29,  # ‡∏≠‡∏á‡∏®‡∏≤‡πÄ‡∏ã‡∏•‡πÄ‡∏ã‡∏µ‡∏¢‡∏™
        "rainfall": 1350,   # ‡∏°‡∏°./‡∏õ‡∏µ
    }

    return satellite_data, weather_data


